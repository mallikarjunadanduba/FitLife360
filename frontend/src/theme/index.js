// Theme selector function
export const getThemeByRole = (role) => {
  switch (role) {
    case 'ADMIN':
      return adminTheme;
    case 'CONSULTANT':
      return consultantTheme;
    case 'USER':
      return userTheme;
    default:
      return defaultTheme;
  }
};
